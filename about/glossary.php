    <a name="gloss"></a>
    <h2 class="text-center">Глоссарий</h2>
    <ul>
      <li><img src="/about/images/glossary/rso.jpg" width="100px"> <u>РСО</u>
        <br> Молодежная общероссийская общественная организация «Российские Студенческие Отряды» (РСО) – крупнейшая молодежная организация страны, которая обеспечивает временной трудовой занятостью более 240 тысяч молодых людей из 72 регионов страны, а также занимается гражданским и патриотическим воспитанием, развивает творческий и спортивный потенциал молодежи
      </li>
      <li><img src="/about/images/glossary/spbso.png" width="100px"> <u>СПбСО</u>
        <br> это надпись на нашивке строевки бойцов санкт-петербургских студенческих отрядов. Что же это значит? А значит это что отряд, у бойцов которого есть такая нашивка, входит в состав городского штаба города Санкт-Петербурга.
      </li>
      <li>
        <u>ЛСО</u>
        <br>- еще одна нашивка. Линейный Студенческий Отряд. Линейный? Да, линейный, один из многих, таких как он. Бойцы носят такую нашивку потому, что они все равны, а особенности нашивок комсоставов описаны чуть в другом месте.
      </li>
      <li>
        <u>СО</u>
        <br>- студенческий отряд- организация студентов, изъявивших желание трудиться на благо общества в различных отраслях хозяйства траны. Отряды работают по разным направлениям:
      </li>
      <li>
        <u>ССО</u>
        <br>- строительные отряды. это группа студентов, выполняющая в летний период строительно-монтажные работы общего профиля где-то за пределами Санкт-Петербурга. Стройотряд - это команда не боящихся трудностей, смелых, сильных людей. Стройка -это незабываемые впечатления, романтика, дружба и дополнительный доход. Хотите чего-то подобного? Тогда стройотряды - это для вас!
      </li>
      <li>
        <u>СПО</u>
        <br>- педагогические отряды. Бойцы таких отрядов несут свет и добро в сердца и умы будущих поколений Эти ребята летом выезжают вожатыми и руководителями детских коллективов в летние оздоровительные лагеря. Тут тебя ждут новые друзья, незабываемое лето и веселая студенческая жизнь! Помимо вожатского мастерства, они занимаются проведением мероприятий, игр, семинаров и тренингов по актуальным проблемам молодежи в современном мире и активно участвуют в студенческой жизни.
      </li>
      <li>
        <u>САО</u>
        <br>- археологические отряды. Участвуют в раскопках памятников древних цивилизации.
      </li>
      <li>
        <u>СОП</u>
        <br>- отряд проводников. Занимаются работой в поездах, проводники пассажирских вагонов поездов, колесящих между городами России
      </li>
      <li>
        <u>СЭО</u>
        <br>-экологический отряд. Что же они делают? Защищают окружающую среду, очищают мир от нашего на него пагубного воздействия. Выполняют различную экологически значимую работу в Заповедниках и Заказниках на территории страны. Это озеленение территорий, экологическое обучение молодёжи, волонтёрские акции.
      </li>
      <li>
        <u>Штаб</u>
        <br>- штаб не зависимо от его уровня, будь то университет или город, занимается развитием движения студенческих отрядов.
        <ul>
          <li>
            Штаб университета организует развитие в пределах университета, помогает отрядам найти помещение для общих сборов и репетиций, если есть возможность-спонсирует деятельность отряда.
          </li>
          <li>
            Городской штаб организует взаимодействие всех отрядов города с городской администрацией, организует масштабные мероприятия между отрядми, взаимодействует с правительством города в сфере молодежной политики.
          </li>
        </ul>
      </li>
      <li>
        <u>Боец</u>
        <br> - человек, входящий в состав отряда, о чем наглядно говорит имеющаяся у него строевочка. Этот человек отработал как минимум один трудовой сезон, заслужив своим трудом уважение коллег, показав высокое качество своей работы. Бойцы всегда готовы помочь человеку в беде, дать совет.
      </li>
      <li>
        <u>Кандидат</u>
        <br>-тот, кто хочет выехать с отрядом на смену/сезон, но происходит это в первый раз. Для кандидатов в студенческие отряды организуют собрания, где бойцы знакомят их со спецификой работы, делятся опытом.
      </li>
      <li>
        <u>Сезон</u>
        <br>- период с момента покидания вокзала/города дпо момент возвращения в него. Для Строителей он называется именно так. Они уезжают на 2 месяца как можно дальше от Санкт-Петербурга например, и не имеют возможности туда вернуться до конца рабочего периода
      </li>
      <li>
        <u>Смена</u>
        <br>- для педагогов рабочее время измеряется не сезонами, а сменами детских лагерей. Человек уехавший на смену находится в состоянии креативной готовности ко всему, нет смысла ему звонить и пытаться связаться. Он с детьми, все 6 его рук очень сильно заняты.
      </li>
      <li>
        <u>Трудовой выезд</u>
        <br>- это короткосрочный выезд отряда с кандидатами на базу либо будущего места работы, либо в место со сходными условиями. Цель такого выезда- ознакомить кандидатов с условиями в которых они будут работать, показать наглядные примеры упрощения, улучшения работы.
      </li>
    </ul>
