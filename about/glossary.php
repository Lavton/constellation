      <span class="T1">        Но время шло, положение в стране улучшалось, а соответственно, улучшалась и жизнь отрядов. В 2000 г. был создан Штаб студенческих отрядов Санкт-Петербурга, и в это же время Политехе было уже 9 отрядов.</span></p>
