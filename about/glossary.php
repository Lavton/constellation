      <span class="T1">        </span></p>
    <p class="P2">
      <span class="T1">И</span>
      <span class="T3">стория студенческих отрядов весьма обширна и насчитывает, по разным источникам, 67 лет или 56 лет.</span>
      <span class="T4"> </span>
      <span class="T3">Чем же обусловлена такая разница? Ответ прост!</span>
      <span class="apple-converted-space">
  <span class="T3"> </span></span>
      <span class="T4"><br/></span>
      <span class="T3">        "Старшими" являются отряды Санкт-Петербурга, отмечающие своё рождение с лета 1948 года, когда 300 студентов Ленинградского политехнического института им. М. И. Калинина отправились на строительство Алакусской ГЭС в Сосновском районе Ленинградской области. А уже в 1949 году выехало около 2-х тысяч студентов, тогда к политехникам присоединились студенты ЛЭТИ, ИТМО и других ВУЗов...</span>
      <span class="apple-converted-space">
  <span class="T3"> </span></span>
      <span class="T4"><br/></span>
      <span class="T3">        Согласно же общероссийской версии, студенческие отряды появились в 1959 году, когда 330 студентов физического факультета Московского государственного университета им. М. В. Ломоносова отправились на целину в Казахстан.</span>
      <span class="apple-converted-space">
  <span class="T3"> </span></span>
      <span class="T4"><br/></span>
      <span class="T3">        На тот момент в СССР существовали только строительные отряды (ССО), а к концу 60-х гг. начали появляться первые педагогические отряды (СПО). Примерно в это же время отрядниками было решено одеваться в строевки (строёвки, целинки, бойцовки, зелёнки, штормовки). </span></p>
    <p class="P1">
      <span class="T1">        Движение росло, в ряды студотрядов вступало всё больше смелых и умелых.. Так, самым активным периодом стали 70-е и 80-е гг, когда в наших рядах насчитывалось около 830 тысяч студентов.</span></p>
    <p class="P1">
      <span class="T1">        Но не всегда жизнь отрядников была полна радостей работы и общения с соратниками: наступили девяностые, тяжелое время, как для отрядов, так и для всей страны. Многие канули в небытие, но остались и те, кто, несмотря на все сложности, продолжали выезжать на сезон. Это были отряды Екатеринбурга и, конечно же, Санкт-Петербурга (
